void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
	if( GPIO_Pin == HMC5883L_DRDY_Pin )
	{
		if ( init_flag )
		{
			uint8_t rx_data[6] = {0};
			int16_t x = 0 , y = 0 , z = 0;
			hmc5883l.read_func(HMC5883L_DOR_X_MSB_REG,rx_data,6U);

			x = (int16_t)(( rx_data[0] << 8U ) | rx_data[1]);
			y = (int16_t)(( rx_data[4] << 8U ) | rx_data[5]);
			z = (int16_t)(( rx_data[2] << 8U ) | rx_data[3]);

			hmc5883l.mag_data.mx 	= 	(double)x * (double)2.56f;
			hmc5883l.mag_data.my 	= 	(double)y * (double)2.56f;
			hmc5883l.mag_data.mz 	= 	(double)z * (double)2.56f;

			// Repeat to start single measurement mode
			uint8_t tx_data = 0x01U;
			hmc5883l.write_func(HMC5883L_MODE_REG,&tx_data,1U);

			/*
			 * @ref Honeywell COMPASS HEADING USING MAGNETOMETERS Application Note
			 */
		    if (hmc5883l.mag_data.my > 0)
		    {
		    	heading = 90.0f - atan(hmc5883l.mag_data.mx / hmc5883l.mag_data.my) * RAD_TO_DEG;
		    }
		    else if (hmc5883l.mag_data.my < 0)
		    {
		    	heading = 270.0 - atan(hmc5883l.mag_data.mx / hmc5883l.mag_data.my) * RAD_TO_DEG;
		    }
		    else if ( (hmc5883l.mag_data.my == 0 ) && ( hmc5883l.mag_data.mx < 0) )
		    {
		    	heading = 180.0f;
		    }
		    else if ( (hmc5883l.mag_data.my == 0 ) && ( hmc5883l.mag_data.mx > 0) )
		    {
		    	heading = 0.0f;
		    }

		    if (heading < 0)
		    {
		    	heading += 360.0f;
		    }
		}

	}
}
